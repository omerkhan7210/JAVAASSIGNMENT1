
public class StringReplacement {
	public static void main(String[] args) {
		String str1 = "This is the sample exercise of OOP basic.I love OOP.";
		String replacedString =  str1.replace("OOP", "DSA");
		System.out.println("This Is The Original String: "+str1);
		System.out.println("This Is The Replaced String: "+replacedString);
	}
}
